/* 
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x)
{
    /* 比较大小 操作减法 */
    int sign = 0x1 << 31; // 避免用逻辑与
    int upperBound = ~0x39;
    int lowerBound = ~0x30 + 1;
    // return ((upperBound + x) >> 31) && !((lowerBound + x) >> 31);
    int upperJudge = ((upperBound + x) & sign) >> 31;
    int lowerJudge = ~((lowerBound + x) & sign) >> 31;
    return !!(upperJudge & lowerJudge);
}
/* 
 * conditional - same as x ? y : z 
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z)
{
    /* 目标是把 x 转换成全 1 全 0 */
    int flag = !!x;
    flag = flag << 31 >> 31; // 全 0 全 1
    return (y & flag) + (z & ~flag); // 注意优先级
}
/* 
 * isLessOrEqual - if x <= y  then return 1, else return 0 
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLessOrEqual(int x, int y)
{
    /* 加法实现减法 */
    // solution 1
    int sign = 0x01 << 31;
    int checkSign = (~x + 1 + y) & sign; // 首位 0 为 x<=y; 首位 1 反之

    // solution 2
    // int diff = ~x + 1 + y;
    // int checkSign = diff >> 31 & 0x01; // 好像会出错 但复现不出来了

    int signX = x & sign;
    int signY = y & sign;
    int signXor = signX ^ signY; // 首位 1 为符号相反; 首位 0 反之

    return !!((!signXor & !checkSign)|(signXor & signX)); // 首位做判断，后续均为 0
}

